# 函數就是指令的意思
# max 指令，功能是取最大值
a = max(123, 456, 789)
print(a)

# len 指令，功能是取字串長度，還可以在其他型態上面
a = len("123456789")
print(a)

# type指令，功能是取型態， 在不知道型態的時候可以用
a = type(123)
print(a)
a = type("123")
print(a)
a = type(True)
print(a)
a = type(123.456)
print(a)

# int指令，功能是轉換成整數
a = int("123")
print(a)
a = int("123.456")
print(a)
a = int(True)
print(a)
a = int(False)
print(a)

# float指令，功能是轉換成浮點數
a = float("123")
print(a)
a = float("123.456")
print(a)
a = float(True)
print(a)
a = float(False)
print(a)

# str指令，功能是轉換成字串
a = str(123)
print(a)
a = str(123.456)
print(a)
a = str(True)
print(a)
a = str(False)
print(a)

# bool指令，功能是轉換成布林值
a = bool(123)
print(a)
a = bool(0)
print(a)
a = bool("")
print(a)
a = bool("123")
print(a)
